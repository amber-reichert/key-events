<p>Congress debates solutions to the issue of slavery's possible expansion into the territories won in the Mexican War. Henry Clay proposes the Compromise of 1850, and Daniel Webster with Stephen Douglas lead its supporters against the measure's opponents who coalesce around John C. Calhoun of South Carolina. </p>
 
<h2>5 9, 1850</h2>
<p>Zachary Taylor dies of "cholera morbus," making him the second President to die in office and vaulting Vice President Millard Fillmore into the White House. </p>
