
<h2>3 4, 1845</h2>""
<p>James K. Polk is inaugurated as the eleventh President of the United States.</p>
<h2>6 25, 1845</h2>""
<p>General Zachary Taylor receives orders from Polk to move his troops from Fort Jesup in Louisiana to a position "on or near the Rio Grande" in Texas to discourage a Mexican invasion.</p> 
<h2>7 1845</h2>""
<p>New York Jacksonian Democrat, John L. O'Sullivan, accuses opponents of Texas annexation of "limiting our greatness and checking the fulfillment of our manifest destiny to overspread the continent allotted by Providence for the free development of our yearly multiplying millions."  The term, "Manifest Destiny," quickly becomes the label used to encapsulate the belief among Americans in their country's God-given right to expand its territory and institutions.</p>
<h2>10 10, 1845</h2>""
<p>Under the direction of Secretary of the Navy George Bancroft, the Naval Academy opens at Annapolis, Maryland.</p> 
<h2>12 29, 1845</h2>""
<p>Texas is admitted as a slave state, making it the twenty-eighth state in the Union.</p> 
<h2>1845-1846</h2>""
<p>The great Irish potato famine forces huge waves of starving immigrants to the United States, sparking anti-Catholic, nativist backlashes.</p> 
<h2>2 4, 1846</h2>""
<p>The Mormon migration to Utah, led by Brigham Young, begins. </p>
<h2>5 13, 1846</h2>""
<p>Congress declares war on Mexico after American troops, under General Zachary Taylor, clash with Mexican troops on the north bank of the Rio Grande. </p>
<h2>6 14, 1846</h2>""
<p>In the Bear Flag Revolt, approximately thirty American settlers (anticipating the Mexican War) take over a small Mexican garrison in Sonoma, California, and declare California a free and independent republic. </p>
<h2>6 15, 1846</h2>""
<p>The Oregon Treaty establishes the 49th parallel as the border between British and American claims to the Oregon Territory, granting the United States clear title to present-day Idaho, Washington, Oregon, and Montana, while granting to Britain territory above the 49th parallel and full control over Vancouver Island. </p>
<h2>7 29, 1846</h2>""
<p>Congress passes the Tariff of 1846, a key part of President's Polk's domestic agenda. Known as the "Walker Tariff," after Polk's secretary of the Treasury, Robert J. Walker, the Tariff of 1846 lowers rates toward revenue-only levels, although a few items remain protected.</p>
<h2>8 3, 1846</h2>""
<p>President Polk vetoes a river and harbors bill which would have provided for federally funded internal improvements. Like Andrew Jackson in his veto of the Maysville Road bill, Polk argues that the bill unfairly favors particular areas, including ports which have no foreign trade. Therefore, says Polk, the bill is unconstitutional.</p>
<h2>8 6, 1846</h2>""
<p>President Polk signs into law the Independent Treasury, which he calls a "Constitutional Treasury," an integral part of his domestic agenda.</p>
<h2>8 10, 1846</h2>""
<p>Congress establishes the Smithsonian Institution. </p>
<h2>11 1846</h2>""
<p>The Whigs regain a majority of seats in the House of Representatives. </p>
<h2>12 28, 1846</h2>""
<p>Iowa is admitted as a free state, making it the twenty-ninth state in the Union.</p> 
<h2>2 22-23, 1847</h2>""
<p>General Zachary Taylor defeats the Mexicans under General Santa Anna at the Battle of Buena Vista. Taylor's victory cements his growing acclaim as a national hero and helps propel him to the 1848 Whig nomination for President. </p>
<h2>7 22, 1847</h2>""
<p>The first Mormon settlers arrive in the Salt Lake Valley. </p>
<h2>9 14, 1847</h2>""
<p> General Winfield Scott takes Mexico City, adding pressure to the Mexican government to submit to the demands of President Polk and sign a treaty of peace. </p>
<h2>1 24, 1848</h2>""
<p> James Marshall discovers gold near Sacramento, California. The discovery begins the massive migrations of the California gold rush, allowing the territory to become a state and setting off fierce debates over whether to admit California as a free or slave state. </p>
<h2>2 2, 1848</h2>""
<p>The Treaty of Guadalupe Hidalgo ends the Mexican War and grants the United States vast territories, including all or large parts of present-day California, Arizona, New Mexico, Colorado, Nevada, Utah, and Wyoming, and confirms the incorporation of Texas as part of the United States. The acquisition of these lands aggravates growing sectionalism in the country over the future of slavery in the Union. </p>
<h2>5 29, 1848</h2>""
<p>Wisconsin is admitted as a free state, making it the thirtieth state in the Union. </p>
<h2>7 12-20, 1848</h2>""
<p>Elizabeth Cady Stanton and Lucretia Mott hold a women's rights convention in Seneca Falls, New York. Among other demands, the convention&rsquo;s "Declaration of Sentiments" calls for women's suffrage.</p>
<h2>11 7, 1848</h2>""
<p>Zachary Taylor is elected the twelfth President of the United States. </p>
<h2>11 1848</h2>""
<p>Democrats regain a majority of seats in the House of Representatives. </p>
<h2>3 3, 1849</h2>""
<p>The United States Department of Interior is created. It combines the General Land Office, the Bureau of Indian Affairs, the Pension Office, the Bureau of the Census, and the Patent Office into a single department. </p>
<h2>3 5, 1849</h2>""
<p>Zachary Taylor is inaugurated as the twelfth President of the United States, and Polk begins a goodwill tour of the South that will eventually end in Tennessee.</p>
