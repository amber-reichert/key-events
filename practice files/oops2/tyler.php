
<h2>4 4, 1841</h2>""
<p>After the death of President William Henry Harrison, Vice President John Tyler assumes the presidency. He is the first ever to do so, setting the precedent for presidential succession.</p>
<h2>9 11, 1841</h2>""
<p>Tyler's entire cabinet, with the exception of Secretary of State Daniel Webster, resigns after Tyler vetoes a second bill for the establishment of a National Bank of the United States. </p>
<h2>8 9, 1842</h2>""
<p>The signing of the Webster-Ashburton Treaty normalizes U.S.-British relations by adjusting the Maine-Brunswick border, settling boundary issues around western Lake Superior, and resurveying numerous smaller borders. </p>
<h2>3 1842</h2>""
<p>The Massachusetts Supreme Court establishes the legality of labor unions, including the right for workers to strike, in the case of <i>Commonwealth v. Hunt</i>. </p>
<h2>1844-1845</h2>""
<p>In the congressional elections, the Democrats gain a majority over the Whigs in the House of Representatives, while at the same time defending their majority in the Senate. </p>
<h2>4 12, 1844</h2>""
<p>The Texas Annexation Treaty is signed by the United States and the Republic of Texas. </p>
<h2>5 24, 1844</h2>""
<p>The first telegraph line in the United States is completed between Washington, D.C., and Baltimore. </p>
<h2>6 8, 1844</h2>""
<p>The Texas Annexation Treaty fails to gain the required two-thirds majority in the Senate amid controversy over the western expansion of the nation. </p>
<h2>6 26, 1844</h2>""
<p>President John Tyler marries Miss Julia Gardiner, becoming the first President to wed while in office. </p>
<h2>12 4, 1844</h2>""
<p>James K. Polk is elected as the eleventh President of the United States on promises to "re-annex" Texas and "re-occupy" Oregon. </p>
<h2>2 28, 1845</h2>""
<p>Congress passes the joint resolution (which only requires a simple majority by both houses) submitted by Tyler to annex Texas. The Republic of Texas votes to accept annexation on June 23. </p>
<h2>3 3, 1845</h2>""
<p>Florida is admitted as a slave state, making it the twenty-seventh state in the Union. </p>
<h2>3 4, 1845</h2>""
<p>James K. Polk is inaugurated as the eleventh President of the United States.</p>
